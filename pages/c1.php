    <h4>Aula 1 - O que é PWA - Progressive Web App.</h4>
    <p>Nesta aula vamos entender o que é <b>PWA - Progressive Web App.</b> </p>
</div>
<br>
<div class="text">
    <p>&nbsp O PWA foi criado pelo Google quando percebeu que muitos  sites estavam sendo acessados pelo celular e surgiu a oportunidade de oferecer  funções parecidas 
    aos de aplicativos nativos. Um PWA é uma aplicação híbrida entre web e mobile. Imagine que ao acessar um site que você goste muito pelo smartphone você receba 
    um aviso para adicionar o site à sua homepage de aplicativos. </p>
    <br>
    <p style="margin: 0;">&nbsp Com o app instalado agora em seu celular, você pode ter a mesma experiência que tinha pelo browser agora sem nenhuma informação em tela além da aplicação, 
    ou seja, toda interface do navegador como barra de endereço, botões, favoritos, etc, são removidos.</p>

    <img alt="" src="imgs/1.PNG" class="img rounded mx-auto d-block">
    
    <p><b>Vantagens do PWA</b> <br> Alguns pontos a se destacar para quem está pensando em criar um PWA:</p>

    <ul>
        <li>Poucas alterações no código do site;</li>
        <li>Utilização de HTML/CSS/Javascript;</li>
        <li>Acesso à API's nativas como geolocalização, câmera, microfone, etc;</li>
        <li>Envio de notificações push;</li>
        <li>Aplicação muito leve (menos de 1MB geralmente);</li>
        <li>Suporte à utilização offline;</li>
        <li>Pontos fracos</li>
    </ul>

    <p>&nbsp Apesar de todas vantagens analisadas acima, os PWA's ainda sofrem com algumas coisas:
    Suporte cross-browser (existem muitos navegadores);</p>

    <ul>
        <li>Sem acesso à vibração, sensores, comunicação com outros apps, etc;</li>
        <li>Não é possível adicioná-los às lojas de aplicativos;</li>
        <li>Interface web pode perder performance em aplicações mais pesadas;</li>
        <li>Pode não passar a legitimidade de uma aplicação mobile;</li>
    </ul>

    <p>&nbsp Neste curso iremos aprender a criar aplicativos PWA com o Glide.</p>
</div>
